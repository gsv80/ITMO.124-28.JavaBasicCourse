package Lesson2;

public class Calculator {
    //sum operations
    public static void sum(Double num1,Double num2){
        System.out.println(num1+num2);
    }
    public static void sum(Long num1, Long num2){
        System.out.println(num1+num2);
    }
    public static void sum(Integer num1,Integer num2){
        System.out.println(num1+num2);
    }

    //subtraction operations
    public static void sub(Double num1,Double num2){
        System.out.println(num1-num2);
    }
    public static void sub(Long num1, Long num2){
        System.out.println(num1-num2);
    }
    public static void sub(Integer num1,Integer num2){
        System.out.println(num1-num2);
    }

    //multiply operations
    public static void mult(Double num1,Double num2){
        System.out.println(num1*num2);
    }
    public static void mult(Long num1, Long num2){
        System.out.println(num1*num2);
    }
    public static void mult(Integer num1,Integer num2){
        System.out.println(num1*num2);
    }

    //dividing operations
    public static void div(Double num1,Double num2){
        System.out.println(num1/num2);
    }
    public static void div(Long num1, Long num2){
        System.out.println(num1/num2);
    }
    public static void div(Integer num1,Integer num2){
        System.out.println(num1/num2);
    }

}
